// Generates test-document/index.html: a screen-reader listening harness.
// Each sample line is rendered four ways (Raw, Visually-hidden, aria-label,
// MathML) so you can arrow down with NVDA/VoiceOver and compare what you hear.
//
// Run: node tools/build-test-doc.mjs

import { writeFile, mkdir } from "node:fs/promises";
import { findTokens } from "../src/core/detect.js";
import { analyze, rewriteWith, SR_ONLY_CSS } from "../src/core/transform.js";

const SAMPLES = [
  "Dissolve 5.0 g of NaCl in 250 mL of H2O.",
  "Prepare 2 mol/L H2SO4 and adjust to pH 7.",
  "Heat CuSO4\u00b75H2O to 80\u00b0C.",
  "The reaction 2H2 + O2 \u2192 2H2O releases \u0394H = \u2212286 kJ/mol.",
  "Add Ca(OH)2(aq) until the solution is neutral.",
  "Concentration of Na+ was 140 mM; glucose (C6H12O6) was 5 mM.",
];

const VARIANTS = [
  { id: "raw", title: "1. Raw (no fix \u2014 what authors usually publish)" },
  { id: "hidden", title: "2. Visually-hidden spoken text (works everywhere, incl. braille)" },
  { id: "aria-label", title: "3. aria-label override (note: changes braille output)" },
  { id: "mathml", title: "4. MathML (NVDA 2026.1 MathCAT / SRE speak + braille)" },
];

function renderLine(text, findings, variantId) {
  if (variantId === "raw") return escapeHtml(text);
  return rewriteWith(text, findings, variantId);
}

function escapeHtml(s) {
  return String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

const sections = SAMPLES.map((text, i) => {
  const { findings } = analyze(text, findTokens);
  const rows = VARIANTS.map(
    (v) => `
        <dt>${escapeHtml(v.title)}</dt>
        <dd>${renderLine(text, findings, v.id)}</dd>`,
  ).join("");
  return `
    <section aria-labelledby="s${i}">
      <h2 id="s${i}">Example ${i + 1}</h2>
      <dl>${rows}</dl>
    </section>`;
}).join("\n");

const html = `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width, initial-scale=1" />
<title>HearSay screen-reader listening test</title>
<style>
  body { font-family: system-ui, sans-serif; max-width: 760px; margin: 2rem auto; padding: 0 1rem; line-height: 1.6; color:#111; }
  h1 { font-size: 1.4rem; }
  h2 { font-size: 1.05rem; margin-top: 1.6rem; border-top: 1px solid #ddd; padding-top: 1rem; }
  dt { font-weight: 600; color:#444; margin-top: .6rem; font-size: .9rem; }
  dd { margin: .2rem 0 0; font-size: 1.05rem; }
  .how { background:#f4f7fb; border:1px solid #d6e0ee; border-radius:8px; padding:1rem; }
  code { background:#eef; padding:0 .25rem; border-radius:3px; }
  math { font-size: 1.1rem; }
  ${SR_ONLY_CSS}
</style>
</head>
<body>
  <h1>HearSay screen-reader listening test</h1>
  <div class="how">
    <p><strong>How to run this test (Windows + NVDA, free):</strong></p>
    <ol>
      <li>Install NVDA from nvaccess.org and start it (<code>Ctrl+Alt+N</code>).</li>
      <li>Open this page in Chrome or Firefox.</li>
      <li>Click the heading, then press the <strong>Down arrow</strong> to read one line at a time.</li>
      <li>For each example, compare what you hear across variants 1\u20134.</li>
      <li>Variant 1 (Raw) should sound wrong; 2\u20134 should sound correct.</li>
    </ol>
    <p>NVDA tip: press <code>NVDA+Down</code> to "say all", or <code>NVDA+Numpad Plus</code> from the top.
       For MathML, NVDA 2026.1+ reads it automatically (MathCAT built in).</p>
  </div>
  ${sections}
  <p style="margin-top:2rem;color:#777;font-size:.85rem">Generated by tools/build-test-doc.mjs</p>
</body>
</html>`;

await mkdir(new URL("../test-document/", import.meta.url), { recursive: true });
await writeFile(new URL("../test-document/index.html", import.meta.url), html, "utf8");
console.log("Wrote test-document/index.html with", SAMPLES.length, "examples.");
