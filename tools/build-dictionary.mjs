// Bundles an NVDA speech dictionary (.dic) into a JS data module so the
// browser (playground + extension) can use it without filesystem access.
//
// Usage: node tools/build-dictionary.mjs [path-to.dic]

import { readFile, writeFile, readdir } from "node:fs/promises";
import { join } from "node:path";

const ROOT = process.cwd();

async function findDic(explicit) {
  if (explicit) return explicit;
  const entries = await readdir(ROOT);
  const dic = entries.find((f) => f.toLowerCase().endsWith(".dic"));
  if (!dic) throw new Error("No .dic file found in project root.");
  return join(ROOT, dic);
}

const dicPath = await findDic(process.argv[2]);
const raw = await readFile(dicPath, "utf8");

const out =
  "// AUTO-GENERATED by tools/build-dictionary.mjs. Do not edit by hand.\n" +
  "// Source: " + dicPath.replace(/\\/g, "/").split("/").pop() + "\n" +
  "export const DICTIONARY_DIC = " + JSON.stringify(raw) + ";\n";

await writeFile(new URL("../src/core/dictionary-data.js", import.meta.url), out, "utf8");
console.log("Bundled", raw.split(/\r?\n/).length, "lines from", dicPath);
